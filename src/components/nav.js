export function renderNav(container, currentPath, navigate) {
  container.innerHTML = `
    <div class="nav-bar">
      <div class="nav-brand" data-path="/">
        <div class="nav-brand-icon">
          <svg width="22" height="22" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
            <path d="M12 20V10"/>
            <path d="M18 20V4"/>
            <path d="M6 20v-4"/>
          </svg>
        </div>
        <span class="nav-brand-text">주제 분석기</span>
      </div>
      <div class="nav-menu">
        ${menuItems.map(item => `
          <button class="nav-item ${currentPath === item.path ? 'active' : ''}" data-path="${item.path}">
            <span class="nav-item-icon">${item.icon}</span>
            <span class="nav-item-label">${item.label}</span>
          </button>
        `).join('')}
      </div>
    </div>
  `;

  // 메뉴 클릭 이벤트
  container.querySelectorAll('[data-path]').forEach(el => {
    el.onclick = () => navigate(el.dataset.path);
  });
}
